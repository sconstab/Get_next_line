#include "libft.h"

char	*ft_strstr(const char *haystack, const char *needle)
{
	size_t	i;
	size_t	j;
	char	*ptr;

	ptr = (char*)haystack;
	if (ft_strlen(needle) == 0)
		return (ptr);
	i = 0;
	while (haystack[i] != '\0')
	{
		j = 0;
		while (needle[j] == haystack[i + j])
		{
			if (needle[j + 1] == '\0')
			{
				ptr = (char*)haystack + i;
				return (ptr);
			}
			j++;
		}
		i++;
	}
	return (NULL);
}
