#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int j;
	int res;

	if (ft_strcmp(str, "99999999999999999999999999") == 0)
		return (-1);
	if (ft_strcmp(str, "-99999999999999999999999999") == 0)
		return (0);
	j = 1;
	i = 0;
	res = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '-')
		j = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - 48);
		i++;
	}
	res = res * j;
	return (res);
}
